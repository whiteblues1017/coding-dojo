package Enum.Cards;

public enum Suits {
    ;

    private String HEART = "H";

    private String DIA = "D";

    private String SPADE = "S";

    private String CLOVER = "C";



    public void setHEART(String HEART) {
        this.HEART = HEART;
    }

    public String getHEART() {
        return HEART;
    }

    public void setDIA(String DIA) {
        this.DIA = DIA;
    }

    public String getDIA() {
        return DIA;
    }

    public void setSPADE(String SPADE) {
        this.SPADE = SPADE;
    }

    public String getSPADE() {
        return SPADE;
    }

    public void setCLOVER(String CLOVER) {
        this.CLOVER = CLOVER;
    }

    public String getCLOVER() {
        return CLOVER;
    }
}
